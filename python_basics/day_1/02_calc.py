#%%
a = 5
b = 4

# Soma
print(f'Soma" {a + b}')

# Subtração
print(f'Subtração" {a - b}')

# Multiplicação
print(f'Multiplicação" {a * b}')

# Divisão
print(f'Divisão" {a / b}')

# Divisão com parte inteira
print(f'Divisão com parte inteira" {a // b}')

# Resto da divisão
print(f'Resto da divisão" {a % b}')

 
# Potência
print(f'Potência: {a ** b}')